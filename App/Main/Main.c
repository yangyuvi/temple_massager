static  void  InitHardware(void);   //初始化硬件相关的模块

/*********************************************************************************************************
*                                              内部函数实现
*********************************************************************************************************/

/*********************************************************************************************************
* 函数名称：InitHardware
* 函数功能：所有的硬件相关的模块初始化函数都放在此函数中
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2018年01月01日
* 注    意：
*********************************************************************************************************/
static  void  InitHardware(void)
{  
  SystemInit();       //系统初始化
  InitPower();        //初始化电源管理
  InitRCC();          //初始化RCC模块
  InitNVIC();         //初始化NVIC模块
  InitKeyOne();       //初始化KeyOne模块
  InitPWM();          //初始化PWM模块
  InitADC();
}


/*********************************************************************************************************
* 函数名称：main
* 函数功能：主函数 
* 输入参数：void
* 输出参数：void
* 返 回 值：int
* 创建日期：2026年05月11日
* 注    意：
*********************************************************************************************************/
int main(void)
{ 
  InitHardware();   //初始化硬件相关函数

  TaskMotorCreate();
  TaskKeyCreate();                      //创建按键任务
  TaskChargeCreate();                   //充电检测
  vTaskStartScheduler();

  while(1);
}
